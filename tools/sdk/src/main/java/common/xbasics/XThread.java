package xpu.sw.tools.sdk.common.xbasics;
//-------------------------------------------------------------------------------------
import java.io.*;

import org.apache.logging.log4j.*;

import xpu.sw.tools.sdk.common.context.*;

//-------------------------------------------------------------------------------------
public class XThread extends XBasic implements Runnable {
    private Thread thisThread;

//-------------------------------------------------------------------------------------
    public XThread(Context _context) {
        super(_context);
    }

//-------------------------------------------------------------------------------------
    public Thread getThisThread() {
        return thisThread;
    }

//-------------------------------------------------------------------------------------
    public void init() {
        thisThread = new Thread(this);
//      return thisThread;
    }

//-------------------------------------------------------------------------------------
    public void start() {
        start(null);
    }

//-------------------------------------------------------------------------------------
    public void start(String _name) {
        if (thisThread == null) {
            init();
        }
        if (_name != null) {
            thisThread.setName(_name);
        }
        thisThread.start();
//      return thisThread;
    }

//-------------------------------------------------------------------------------------
    public void run() {
    }

//-------------------------------------------------------------------------------------
    public void interrupt() {
        thisThread.interrupt();
    }

//-------------------------------------------------------------------------------------
    public void load() {
    }

//-------------------------------------------------------------------------------------
    public void save() {
        if (thisThread != null) {
            thisThread.interrupt();
        }
    }

//-------------------------------------------------------------------------------------
}
